package com.kotcrab.vis.runtime.util.annotation;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * Methods annotated by this annotation are considered internal to Vis framework and should not be called manually.
 * It's not guaranteed that those methods won't change between versions.
 * @author Kotcrab
 */
@Retention(RetentionPolicy.SOURCE)
@Target(ElementType.METHOD)
public @interface VisInternal {
}
